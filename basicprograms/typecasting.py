a = "1"
b = "8"
# Explicit Typecasting
print("Addition of", a, "and", b, "is", int(a)+int(b))
print("Subtraction of", a, "and", b, "is", int(a)-int(b))
print("Multiplication of", a, "and", b, "is", int(a)*int(b))

c = 4.5
d = 9
# Implicit Typecasting : It will give float value cause it is of higher order type
print("Addition of", c, "and", d, "is", c+d) 
print("Subtraction of", c, "and", d, "is", c-d)
print("Multiplication of", c, "and", d, "is", c*d)
