# Python program demonstrating array operations

# Create an array (list)
numbers = [10, 20, 30, 40, 50]

# Append an element
numbers.append(60)

# Access elements by index
first = numbers[0]
last = numbers[-1]

# Remove an element
numbers.remove(30)

# Get array length
length = len(numbers)

# Print results
print("Array:", numbers)
print("First element:", first)
print("Last element:", last)
print("Array length:", length)
