def check_number(number):
  """Checks if a number is positive, negative, or zero and prints the result."""
  if number > 0:
    print("The number is positive.")
  elif number < 0:
    print("The number is negative.")
  else:
    print("The number is zero.")

# Example usage:
check_number(5)
check_number(-2)
check_number(0)
