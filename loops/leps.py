# Example of a for loop
print("For Loop Example:")
for i in range(5):
    print(f"Iteration {i}")

# Example of a while loop
print("\nWhile Loop Example:")
count = 0
while count < 5:
    print(f"Count is {count}")
    count += 1

# Using loops to calculate a sum
print("\nCalculating Sum with a For Loop:")
numbers = [1, 2, 3, 4, 5]
total = 0
for num in numbers:
    total += num
print(f"Sum of numbers is {total}")

