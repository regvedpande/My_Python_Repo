# This code demonstrates a simple for loop in Python

print("Starting the loop demonstration:")

# We'll iterate through a list of fruits
fruits = ["apple", "banana", "cherry", "date"]

# The for loop will go through each item in the 'fruits' list
for fruit in fruits:
  print(f"I love {fruit}!") # f-string for easy formatting

print("\nLoop finished!")
print("This loop printed a message for each fruit in the list.")
